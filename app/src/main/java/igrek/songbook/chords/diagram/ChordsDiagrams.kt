val allPianoChordsNames: Map<String, List<String>> by lazy {
    val notes = listOf(
        "C",
        "C#",
        "Db",
        "D",
        "D#",
        "Eb",
        "E",
        "E#",
        "F",
        "F#",
        "Gb",
        "G",
        "G#",
        "Ab",
        "A",
        "A#",
        "Bb",
        "B",
        "Cb",
    )
    val suffixes = PianoChordDiagramBuilder.chordTypeSequences.keys
    val diagrams = hashMapOf<String, List<String>>()
    notes.forEach { note ->
        suffixes.forEach { suffix ->
            val chordName = note + suffix
            diagrams[chordName] = emptyList()
        }
    }
    diagrams
}

private fun populateAllChordDiagrams(baseMap: Map<String, List<String>>): Map<String, List<String>> {
    val diagrams = HashMap(baseMap)
    // populate aliases
    chordDiagramPrefixAliases.forEach { (aliasPrefix, aliasedTypes) ->
        baseMap.filterKeys { it.startsWith(aliasPrefix) }
            .forEach { (chord, definitions) ->
                val chordSuffix = chord.drop(aliasPrefix.length)
                aliasedTypes.forEach { aliasedType ->
                    val newChordName = aliasedType + chordSuffix
                    diagrams[newChordName] = definitions
                }
            }
    }
    return diagrams
}
